package sharedhouse;

public class AdminInfo {
		private String adName;
		private String adPass;
		public AdminInfo(){
			  //定义一个不带参数构造方法
		  }  
	  public String getAdName() {
			return adName;
		}
		public void setAdName(String adName) {
			this.adName = adName;
		}
		public String getAdPass() {
			return adPass;
		}
		public void setAdPass(String adPass) {
			this.adPass = adPass;
		}

}
